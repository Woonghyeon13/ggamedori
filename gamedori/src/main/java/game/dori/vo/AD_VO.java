package game.dori.vo;

public class AD_VO {

	private int ad_idx;
	private String ad_img_b;
	private String ad_img_s;
	private String ad_link;
	private String ad_title;
	private String ad_contents;
	
	public int getAd_idx() {
		return ad_idx;
	}
	public void setAd_idx(int ad_idx) {
		this.ad_idx = ad_idx;
	}
	public String getAd_img_b() {
		return ad_img_b;
	}
	public void setAd_img_b(String ad_img_b) {
		this.ad_img_b = ad_img_b;
	}
	public String getAd_img_s() {
		return ad_img_s;
	}
	public void setAd_img_s(String ad_img_s) {
		this.ad_img_s = ad_img_s;
	}
	public String getAd_link() {
		return ad_link;
	}
	public void setAd_link(String ad_link) {
		this.ad_link = ad_link;
	}
	public String getAd_title() {
		return ad_title;
	}
	public void setAd_title(String ad_title) {
		this.ad_title = ad_title;
	}
	public String getAd_contents() {
		return ad_contents;
	}
	public void setAd_contents(String ad_contents) {
		this.ad_contents = ad_contents;
	}
	
	
}
